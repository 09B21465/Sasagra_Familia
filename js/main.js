function keyDownHandler(e) {
    if(e.key == "Right" || e.key == "ArrowRight") {
        rightPressed = true;
    }
    else if(e.key == "Left" || e.key == "ArrowLeft") {
        leftPressed = true;
    }
    else if(e.key == "Up" || e.key == "ArrowUp") {
        upPressed = true;
    }
    else if(e.key == "Shift"){
        shiftPressed = true;
    }
}

function keyUpHandler(e) {
    if(e.key == "Right" || e.key == "ArrowRight") {
        rightPressed = false;
    }
    else if(e.key == "Left" || e.key == "ArrowLeft") {
        leftPressed = false;
    }
    else if(e.key == "Up" || e.key == "ArrowUp"){
        upPressed = false;
    }
    else if(e.key == "Shift"){
        shiftPressed = false;
    }
}

function draw(user, enemy) {
    // console.log("draw");
    if(drawPointX + 16 > user.xPos){
        drawPointX = user.xPos - 16;
    }else if(drawPointX + 64 < user.xPos){
        drawPointX = user.xPos - 64;
    }if(drawPointX < 0) drawPointX = 0;
    
    ctx.clearRect(0, 0, canvas.width, canvas.height);

    for(let i = 0; i < 15; i++){
        for(let j = 0; j < 256; j++){
            if(mapData[i][j] == 1){
                ctx.drawImage(floor_img,map_tip_size*j-drawPointX, map_tip_size * i);
            }else if(mapData[i][j] == 2){
                ctx.drawImage(tile1_img,map_tip_size*j-drawPointX, map_tip_size * i);
            }else if(mapData[i][j] == 3){
                ctx.drawImage(item_img,map_tip_size*j-drawPointX, map_tip_size * i);
            }else if(mapData[i][j] == 4){
                ctx.drawImage(tile2_img,map_tip_size*j-drawPointX, map_tip_size * i);
            }
        }
    }

    if(user.dir == 0){ctx.drawImage(user.img, 63, 0, 64, 48, user.xPos - drawPointX, user.yPos, user.size, user.size);}
    else if(user.dir == 1){ctx.drawImage(user.img, 0, 0, 64, 48, user.xPos - drawPointX, user.yPos, user.size, user.size);}
    else if(user.dir == 2){ctx.drawImage(user.img, 126, 0, 64, 54, user.xPos - drawPointX, user.yPos, user.size, user.size);}

    for(i = 0; i < enemy.length; i++){
        ctx.beginPath();
        ctx.drawImage(enemy[i].img, enemy[i].xPos - drawPointX, enemy[i].yPos, enemy[i].size, enemy[i].size);
        ctx.closePath();
    }

    for(i = 0; i < item.length; i++){
        if(item[i].visible){
            ctx.beginPath();
            ctx.drawImage(item[i].img, item[i].xPos - drawPointX, item[i].yPos, item[i].size, item[i].size);
            ctx.closePath();
        }
    }

    ctx.font = ' 48px serif';
    ctx.fillText(String(time), 20, 50)
    time_counter += 1;
    if(time_counter == fps){
        time -= 1;
        time_counter = 0;
    }
}

